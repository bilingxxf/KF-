<p>1. 什么是Ripple (XRP)？</p>
 
<p>瑞波币（Ripple/XRP）提供一个无阻碍的利用区块链技术的全球支付网络，是世界上第一个开放的支付网络，通过加入Ripple正在成长的全球支付网络可以转账任意一种货币，包括美元、欧元、人民币、日元或者比特币，简便易行快捷，交易确认在几秒以内完成，交易费用几乎是零，没有所谓的跨行异地以及跨国支付费用。 Ripple是开放源码的点到点支付网络，它可以使任何机构或个人轻松、廉价并安全的把金钱转账到互联网上的任何机构或个人，无论在世界的哪个地方。Ripple是p2p软件，没有任何个人、公司，或政府操控，任何人可以创建一个ripple账户。</p>
 
<p>自2017年5月以来，瑞波的验证者网络已经扩大了140%，达到了55个参与者，其中包括WorldLink、Telindus-Proximus集团、微软、MIT、CGI、瑞典ISP、Bahnhof以及数据中心和系统整合服务提供商AT TOKYO等。而所有这些参与者现都正在维护着XRP分类账本（此前成为瑞波共识账本）的完整性，运行软件帮助其对交易结算订单达成一致。</p>
