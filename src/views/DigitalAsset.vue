<p>二、项目介绍</p>
<p>IOTA背后的主要创新是Tangle。这是一种基于DAG（有向无环图）的全新分布式账本体系结构。有人可能会把它称为“Block and the Chain and Block”（语义上，它不是真正的区块链）。</p>
<p>在它的核心上，Tangle仍然与区块链有着相同的基本原则：它仍然是一个分布式数据库，它仍然是一个P2P网络，它仍然依靠共识和验证机制。</p>
<p>但是，如果我们要概括Tangle和区块链之间的主要区别，最明显的两个是Tangle是如何构建的（一个DAG），以及我们如何达成共识。</p>
<p>IOTA基本上毫无“区块” 的概念。相反，每笔交易会参考之前两笔交易。这种交易的参考被视为一种证明：您直接证明您的交易证明了两笔交易，并且间接地证明该交易的一小部分是有效的且符合协议规则。</p>
<p>与其让网络的小部分负责给共识，全部网络活跃参与者的（矿工）直接参与交易的批准。因此，IOTA的共识不再与交易制定过程脱钩：这是它的一个固有部分，它使IOTA能够在没有任何交易费的情况下进行扩展。</p>
<br />

<p>IOTA中的交易过程是一个简单的3步过程：</p>
<br />

<p>1. 签名：您使用您的私钥签署交易输入</p>
<p>2. 提示选择：MCMC（Markov chain Monte Carlo）用于随机选择两个提示（未确认的交易），这些提示将被您的交易（branchTransaction    和 trunkTransaction）</p>
<p>3. 工作证明：为了让您的交易被网络接受，您需要做一些工作证明 - 类似于Hashcash（spam和sybil-resistance）</p>
 
<p>一旦完成了，交易将被广播到网络。其他人会来，在提示选择过程中选择您的交易并验证它。就这样，你的交易得到确认。</p>
<p>由于IOTA在没有任何矿工参与的情况下就交易的有效性达成共识，我们也没有交易费用支付。 IOTA是第一个交易结算协议，它使您可以为发送方或接收方交易甚至是亚点值的点对点，而不需要任何交易费用。因此，我们确实认为IOTA是所有当前和未来小额支付和纳米支付使用案例的支柱。</p>
<p>IOTA旨在实现规模交易结算。由于共识是并行的，而不是像区块链那样在批次的连续间隔内完成，网络能够随交易数量动态增长和扩展。交易越多，Tangle获得的安全性越高，效率越高。</p>
<p>IOTA创建的主要原因是为了实现和成为机器经济的中坚力量。我们设想未来机器在没有任何第三方参与的情况下交换资源（计算，电力，存储，带宽，数据等）和服务，纯粹是机器对机器。随着物联网开始自我释放，对“智能分权”的需求显而易见。</p>
